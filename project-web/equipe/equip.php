        <h1> Big data et systemes decisionnels (BDD)</h1>
        <p>
            <span>Mission:</span>  <br>
            Conscients que l'une des plus grandes richesses d'une entreprise est son information, mais 
            noyés sous de nombreuses données, éparses, déstructurées et hétérogènes, les dirigeants sont
            face à une problématique de taille : comment analyser ces informations, dans des temps raisonnables 
            et avec le maximum de cohérence. Ces décideurs ont besoin qu'on leur expose les faits importants,
            base de leurs décisions. <br>
            
            C'est l’objectif de l'informatique décisionnelle (aussi nommée DSS pour Decision Support System ou 
            encore BI pour Business Intelligence). Elle prend une place en constante croissance dans les systèmes 
            d'information (SI) depuis son apparition, dans les années quatre-vingt-dix. <br>
            
            Elle permet une sélection des informations opérationnelles pertinentes pour l'entreprise. Celles-ci 
            sont ensuite normalisées pour alimenter un entrepôt de données. Ces dernières années, beaucoup de 
            travaux se sont intéressés à ce concept et ont tenté de rendre les données d'un entrepôt cohérentes,
            lisibles, intelligibles et faciles d'accès. Les big data, littéralement les grosses données désignent
            des ensembles de données qui deviennent tellement volumineux qu'ils en deviennent difficiles à 
            travailler avec des outils classiques de gestion de base de données ou de gestion de l'information. 
            Dans ces nouveaux ordres de grandeur, la capture, le stockage, la recherche, le partage, l'analyse 
            et la visualisation des données doivent être redéfinis car elles doivent porter sur des données quantitatives
            complexes (volumineuses, variées, non structurées) avec des méthodes de calcul distribué. <br>
            
            L’objectif de nos travaux est de marier les deux concepts big data et systèmes décisionnels afin d’apporter 
            des solutions qui permettront de représenter, stocker et restituer les big data afin de les utiliser dans une 
            perspective décisionnelle. <br>

        </p>
        <p><span>Mots Clés:</span>  <br>
            Big Data, Systèmes décisionnels, Informatique, Entrepôt de données.</p></p>
