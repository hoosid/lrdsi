        <h1> Securite de systemes informatiques et raisonnement (SIIR)</h1>
        <p>
            <span>Mission:</span>  <br>
            Le marché de la sécurité foisonne d’offres, de technologies, d’acteurs et l’actualité de la cybercriminalité 
            est riche. Il ne se passe pas une journée sans que de nouveaux virus, de nouvelles failles sur des logiciels 
            ou systèmes soient annoncés ou que des systèmes stratégiques soient piratés.
            
            Le concept de sécurité des systèmes d’information recouvre un ensemble de méthodes, techniques et outils 
            chargés de protéger les ressources d’un système d’information afin d’assurer:
            <br>
        - La disponibilité des services: les services (ordinateurs, réseaux, périphériques, applications…) et
            les informations (données, fichiers…) doivent être accessibles aux personnes autorisées quand elles en ont besoin;</li>
            <br> La confidentialité des informations: les informations n’appartiennent pas à tout le monde; seuls peuvent
            y accéder ceux qui en ont le droit;</li>
            <br>- L’intégrité des systèmes : les services et les informations (fichiers, messages…) ne peuvent être modifiés 
            que par les personnes autorisées (administrateurs, propriétaires…).
            <br>
            Les chercheurs de l'équipe SIIR étudient les systèmes d'information et de communications afin de mettre 
            en place des systèmes d'analyse et de contrôle. <br>
            
            Notre but est résumé en les points suivants: <br>
            
            - Mise en place d'un cadre et d'outils logiciels pour le développement formel et systématique des
            protocoles de sécurité. <br>
            - Élaboration de techniques formelles et d'outils informatiques pour la modélisation et la vérification 
            des systèmes de détection d'intrusions, de corrélation d'alertes et de systèmes de contrôle d'accès. <br>
            - Appliquer ces méthodes pour le test de vulnérabilité et l'audit de systèmes informatiques. <br>
            
            Nos travaux peuvent avoir plusieurs domaines d'application: E-health, les réseaux sociaux, e-commerce, <br>
            vote électronique, Cloud computing, ... <br>
            

        </p>
    <p><span>Mots Clés:</span>  <br>
        Système d'information, sécurité, raisonnement, détection d'intrusions, contrôle d'accès.</p></p>
