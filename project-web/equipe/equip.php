        <h1> Reconnaissance des formes et applications (RFA)</h1>
        <p>
            <span>Mission:</span>  <br>
            Le travail de recherche mené par les membres de cette équipe couvre un large champ de méthodologies 
            pour apporter des solutions à des domaines applicatifs liés à la reconnaissance des formes qui consiste
            à concevoir des outils automatiques, informatiques, capables de reconnaître des formes, ou patterns.
            Les chercheurs s’intéressent au développement de systèmes intelligents de reconnaissance de forme pour
            des applications réelles. Il s’agit de résoudre des problèmes pratiques dans les domaines de recherche
                divers tels que : la télédétection, la santé, la vidéosurveillance, la sécurité informatique,
                la biométrie, la classification et la catégorisation des documents et des données multimédia. <br>
            
            Les méthodes mises en œuvre dans les systèmes de RdF s'appuient sur de nombreux domaines: l'analyse 
            numérique, les statistiques, l'optimisation combinatoire, la recherche opérationnelle, l'analyse 
            syntaxique, la théorie des graphes, l'intelligence artificielle, .. <br>
            
            La Reconnaissance des formes est utilisée dans de nombreux domaines d'applications tels que:
            l'analyse de signaux sismiques, l'analyse d'électrocardiogrammes, la reconnaissance de la parole,
            la reconnaissance de l'écriture, l'analyse de documents, imagerie médicale (microscopique, radiographique,
            IRM, ...), imagerie satellitaire, la biométrie (reconnaissance d'empreintes, de faces, ...), la vision par
            ordinateur (analyse de scènes 3D). Parmi les objectifs visés par notre équipe, nous œuvrons bien évidemment à : <br>

            - Valider les outils proposés par les chercheurs ; <br>
            - Communiquer et publier les travaux effectués dans les revues reconnues à l’échelle internationale 
            ainsi que dans les actes de conférences indexées ; <br>
            - Appliquer nos travaux aux secteurs socio-économiques, en particulier ceux liés à : la santé ;
            l’environnement ; l’agriculture ; la sécurité, ..etc <br>
        </p>
        <p><span>Mots Clés:</span>  <br>
            Reconnaissance de formes, Apprentissage automatique, Indexation et Annotation pour la
        Recherche d’Information, Intelligence Artificielle, Traitement d’images, Filtrage de 
        l'information, Inférence statistique, Vision Artificielle, 
        Fouille de Données, Méta-heuristiques et Approches évolutionnaires, Aide à la décision, ..</p>
    </p>
