                    Le 3ème cycle LMD ‘Systèmes Informatiques ‘ </li>

                <li> 3ème cycle LMD ‘Génie des Systèmes Informatiques ‘ Option ‘Informatique Répartie et Mobile’’ </li>

                <li>Le 3ème cycle LMD ‘‘Informatique Décisionnelle’’, Option ‘‘Entreposage et fouille de données complexes’’ </li>
                <li>’école Doctorale des Sciences et Technologie de l’Information et de la Connaissance ‘EDSTIC’ en partenariat avec l’Ecole Supérieure d’Informatique ESI. Le laboratoire a accueilli les post graduants des deux options, à savoir ‘‘Systèmes
                    d’Information et de la Connaissance (SIC)’’ et ‘‘ l’Informatique Répartie et Mobile (IRM)’’ </li>
                <li>Le magister ‘Ingénierie des Systèmes et de la Connaissance’’ </li>
                <li>Le doctorat classique </li>
