            <p>
                La formation à et par la recherche est une mission importante du laboratoire LRDSI. Ses membres sont impliqués dans différentes formations dispensées au sein du département d’informatique. Ils proposent également aux étudiants de licence, de master ou
                de doctorat des projets en lien avec leurs activités de recherche.
                <br>
                <br>Dès sa création, le LRDSI a été impliqué dans différentes formations post graduées dont : <br>
                <br>
            </p>
